package cn.sf.qrcode.webview.domain;

public class ViewVO{

	private String alipay;

	private String alipayShow;

	
	private String person;

	
	private String personShow;
	
	private String name;
	
	private Boolean isShowPerson;
	
	private Boolean isShowAlipay;

	public String getAlipay() {
		return alipay;
	}

	public void setAlipay(String alipay) {
		this.alipay = alipay;
	}

	public String getAlipayShow() {
		return alipayShow;
	}

	public void setAlipayShow(String alipayShow) {
		this.alipayShow = alipayShow;
	}

	public String getPerson() {
		return person;
	}

	public void setPerson(String person) {
		this.person = person;
	}

	public String getPersonShow() {
		return personShow;
	}

	public void setPersonShow(String personShow) {
		this.personShow = personShow;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Boolean getIsShowPerson() {
		return isShowPerson;
	}

	public void setIsShowPerson(Boolean isShowPerson) {
		this.isShowPerson = isShowPerson;
	}

	public Boolean getIsShowAlipay() {
		return isShowAlipay;
	}

	public void setIsShowAlipay(Boolean isShowAlipay) {
		this.isShowAlipay = isShowAlipay;
	}
	
}
